package com.qa.day2;

public class WhileDoWhile {
	public static void whilewhile() {
		int count = 0;
		while(count > 10) {
			count++;
			System.out.println("count = " + count );
		}
	}
	
	public static void dodowhilewhile() {
		int count = 0;
		do {
			System.out.println("weewooo");
		} while(count > 10);
	}
}
